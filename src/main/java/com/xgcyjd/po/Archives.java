package com.xgcyjd.po;

public class Archives {
   private int id;
   private int archives_detail_id;
   private int user_id;
   private int state;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getArchives_detail_id() {
        return archives_detail_id;
    }

    public void setArchives_detail_id(int archives_detail_id) {
        this.archives_detail_id = archives_detail_id;
    }

    public int getUser_id() {
        return user_id;
    }

    public void setUser_id(int user_id) {
        this.user_id = user_id;
    }

    public int getState() {
        return state;
    }

    public void setState(int state) {
        this.state = state;
    }
}
